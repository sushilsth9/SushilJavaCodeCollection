package com.company;

public class operatorpractice {
    public static void main(String[] args) {
        int a = 5;//6
        int b = 3;//3
        int c = 7;//7
        int d = 2;//2
        int e = 4;//4
        System.out.println(++a);
        System.out.println(+b);
        System.out.println(b--);
        System.out.println(--c);
        System.out.println(+d);
        System.out.println(d);
        System.out.println(d++);
        System.out.println(e--);


      //  int result = ++a + b-- - --c + d++ * e--;
    }
}
